public class CalculadoraSimples extends JFrame {
    private JTextField txtDisplay;
    private double num1, num2, resultado;
    private String operacao;

    public CalculadoraSimples() {
        setSize(300, 150);
        setDefaultCloseOperation(EXIT_ON_CLOSE);
        setLocationRelativeTo(null);
        setLayout(null);

        txtDisplay = new JTextField();
        txtDisplay.setBounds(20, 20, 250, 30);
        add(txtDisplay);

        JButton btnMais = new JButton("+");
        btnMais.setBounds(20, 60, 50, 30);
        add(btnMais);

        JButton btnIgual = new JButton("=");
        btnIgual.setBounds(80, 60, 50, 30);
        add(btnIgual);

        // Quando clicar no botÃ£o +
        btnMais.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                try {
                    num1 = Double.parseDouble(txtDisplay.getText());
                    operacao = "+";
                    txtDisplay.setText("");
                } catch (NumberFormatException ex) {
                    txtDisplay.setText("Erro");
                }
            }
        });

        // Quando clicar no botÃ£o =
        btnIgual.addActionListener(new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                try {
                    num2 = Double.parseDouble(txtDisplay.getText());
                    if (operacao.equals("+")) {
                        resultado = num1 + num2;
                    }
                    txtDisplay.setText(String.valueOf(resultado));
                } catch (Exception ex) {
                    txtDisplay.setText("Erro");
                }
            }
        });
    }

    public static void main(String[] args) {
        new CalculadoraSimples().setVisible(true);
    }
    }//GEN-LAST:event_ButtonIgualdadeActionPerformed

